// THERE ARE TWO TYPE OF CODE
// 1.Synchronous / blocking :-
        // line by line execution of the code
        // like in the sequence in which you write the code, in the same sequence the output will be printed  
// 2.Asynchronous / non-blocking :-
        // works on callbacks
        // it does not alway will give you the output in the same sequence in which you write your code
        // for ex- if particular function is taking time to execute then it will keep loading until then code below it will get exicute and print
        // and once the function is loaded fully it will get printed later.

